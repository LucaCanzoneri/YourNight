package com.example.yournight;

public class ElementList {
    public String title;
    public String valutazione;
    public String indirizzo;
    public String immagine;
    public String numero;

    public ElementList(String mTitle, String mValutazione, String mIndirizzo, String mImmagine, String mNumero) {
        title = mTitle;
        valutazione = mValutazione;
        indirizzo = mIndirizzo;
        immagine = mImmagine;
        numero = mNumero;
    }

}
